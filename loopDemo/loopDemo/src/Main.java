public class Main {
    public static void main(String[] args) {
        //for

        for(int i=1;i<=10;i++) {

            System.out.println(i);

        }

        System.out.println(" For Döngüsü Bitti ");

        int i=1;

        //while

        while (i<10){

                System.out.println(i);

                i+=2;
        }
        System.out.println(" DOo - While Döngüsü Bitti ");

        //DOo-While
        int j=100;
        do{

            System.out.println(j);
                j+=2;
        }while (j<10);
    }
}






